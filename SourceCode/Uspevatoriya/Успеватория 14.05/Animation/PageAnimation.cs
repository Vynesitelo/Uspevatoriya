
namespace Успеватория
{
    /// <summary>
    /// Стили анимации страниц ддля появления и исчезновения
    /// </summary>
    public enum PageAnimation
    {
        /// <summary>
        /// Без анимации
        /// </summary>
        None = 0,

        /// <summary>
        /// Страница открывается и исчезает справа
        /// </summary>
        SlideAndFadeInFromRight = 1,

        /// <summary>
        /// Страница открывается и исчезает Слева
        /// </summary>
        SlideAndFadeOutToLeft = 2,
    }
}
